def matrix_addition_reloaded(*matrices)
  height = matrices.first.length
  width = matrices.first.first.length
  matrices.each do |matrix| 
    return nil unless matrix.length == height
    matrix.each {|row| return nil unless row.length == width}
  end
  new_matrix = Array.new(height) {Array.new(width)}
  height.times do |i|
    width.times do |j|
      sum = matrices.reduce(0) {|memo, matrix| memo + matrix[i][j]} 
      new_matrix[i][j] = sum
    end
  end
  new_matrix
end

matrix_a = [[2,5], [4,7]]
matrix_b = [[9,1], [3,0]]
matrix_c = [[-1,0], [0,-1]]
matrix_d = [[2, -5], [7, 10], [0, 1]]
matrix_e = [[0 , 0], [12, 4], [6,  3]]

p matrix_addition_reloaded(matrix_a, matrix_b)              # [[11, 6], [7, 7]]
p matrix_addition_reloaded(matrix_a, matrix_b, matrix_c)    # [[10, 6], [7, 6]]
p matrix_addition_reloaded(matrix_e)                        # [[0, 0], [12, 4], [6, 3]]
p matrix_addition_reloaded(matrix_d, matrix_e)              # [[2, -5], [19, 14], [6, 4]]
p matrix_addition_reloaded(matrix_a, matrix_b, matrix_e)    # nil
p matrix_addition_reloaded(matrix_d, matrix_e, matrix_c)    # nil


def squarocol?(matrix)
  matrix.each do |sub|
    return true if sub.all? {|el| el == sub[0]}
    sub.each_with_index {|el, i| return true if matrix.all? {|arr| arr[i] == el}}
  end
  false
end

p squarocol?([
    [:a, :x , :d],
    [:b, :x , :e],
    [:c, :x , :f],
]) # true

p squarocol?([
    [:x, :y, :x],
    [:x, :z, :x],
    [:o, :o, :o],
]) # true

p squarocol?([
    [:o, :x , :o],
    [:x, :o , :x],
    [:o, :x , :o],
]) # false

p squarocol?([
    [1, 2, 2, 7],
    [1, 6, 6, 7],
    [0, 5, 2, 7],
    [4, 2, 9, 7],
]) # true

p squarocol?([
    [1, 2, 2, 7],
    [1, 6, 6, 0],
    [0, 5, 2, 7],
    [4, 2, 9, 7],
]) # false


def squaragonal?(matrix)
  first = matrix[0][0]
  last = matrix[0][-1]
  j = matrix[0].length - 1
  matrix.each_with_index do |sub, i|
    return false unless sub[i] == first || sub[j] == last
    j -= 1
  end
  matrix.each
  true
end

