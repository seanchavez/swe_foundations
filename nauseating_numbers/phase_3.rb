def pascals_triangle(n)
  triangle = [[1]]
  until triangle.length == n
    row = [1]
    (0...triangle.last.length - 1).each do |i|
      row << triangle.last[i] + triangle.last[i + 1]
    end
    triangle << (row << 1)
  end
  triangle
end

