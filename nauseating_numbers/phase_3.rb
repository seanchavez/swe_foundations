def squarocol?(matrix)
  matrix.each do |sub|
    return true if sub.all? {|el| el == sub[0]}
    sub.each_with_index {|el, i| return true if matrix.all? {|arr| arr[i] == el}}
  end
  false
end

p squarocol?([
    [:a, :x , :d],
    [:b, :x , :e],
    [:c, :x , :f],
]) # true

p squarocol?([
    [:x, :y, :x],
    [:x, :z, :x],
    [:o, :o, :o],
]) # true

p squarocol?([
    [:o, :x , :o],
    [:x, :o , :x],
    [:o, :x , :o],
]) # false

p squarocol?([
